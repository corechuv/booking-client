const DEFAULT_LANGUAGE: AppLanguageCode = 'ru'

const fillTemplate = (template: string, params?: TranslationParams): string => {
  if (!params) {
    return template
  }

  return Object.entries(params).reduce((result, [key, value]) => {
    return result.replaceAll(`{${key}}`, String(value))
  }, template)
}

export const translate = (
  language: AppLanguageCode,
  key: string,
  params?: TranslationParams,
): string => {
  const langDictionary = messages[language] ?? messages[DEFAULT_LANGUAGE]
  const template = langDictionary[key] ?? messages[DEFAULT_LANGUAGE][key] ?? key
  return fillTemplate(template, params)
}

export const hasTranslation = (language: AppLanguageCode, key: string): boolean =>
  Boolean(messages[language]?.[key])
